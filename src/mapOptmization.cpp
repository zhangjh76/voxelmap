    void BuildVoxelMap(){
        pcl::PointCloud<pcl::PointXYZI>::Ptr world_lidar(new pcl::PointCloud<pcl::PointXYZI>);
        TransformLidar2World(laserCloudSurfelLastDS, world_lidar);
        std::vector<pointWithCov> pv_list;
        for (size_t i = 0; i < world_lidar->size(); i++) {
            pointWithCov pv;
            pv.point << world_lidar->points[i].x, world_lidar->points[i].y,
                    world_lidar->points[i].z;
            Eigen::Vector3d point_this(laserCloudSurfelLastDS->points[i].x,
                        laserCloudSurfelLastDS->points[i].y,
                        laserCloudSurfelLastDS->points[i].z);
            // if z=0, error will occur in calcBodyCov. To be solved
            if (point_this[2] == 0) {
                point_this[2] = 0.001;
            }
            Eigen::Matrix3d cov;
            calcBodyCov(point_this, ranging_cov, angle_cov, cov);

            // point_this += Lidar_offset_to_IMU;
            Eigen::Matrix3d point_crossmat;
            point_crossmat << SKEW_SYM_MATRX(point_this);
            // provided by imu , to be added
            cov = R * cov * R.transpose() +  
                (-point_crossmat) * Eigen::Matrix3d::Identity() *
                (-point_crossmat).transpose() * INIT_COV +
                Eigen::Matrix3d::Identity() * INIT_COV;
            pv.cov = cov;
            pv_list.push_back(pv);
            Eigen::Vector3d sigma_pv = pv.cov.diagonal();
            sigma_pv[0] = sqrt(sigma_pv[0]);
            sigma_pv[1] = sqrt(sigma_pv[1]);
            sigma_pv[2] = sqrt(sigma_pv[2]);
        }

        uint plsize = pv_list.size();
        for (uint i = 0; i < plsize; i++) {
            const pointWithCov& p_v = pv_list[i];

            double loc_xyz[3];
            for (int j = 0; j < 3; j++) {
                loc_xyz[j] = p_v.point[j] / voxel_size;
                if (loc_xyz[j] < 0) {
                    loc_xyz[j] -= 1.0;
                }
            }
            VOXEL_LOC position((int64_t) loc_xyz[0], (int64_t) loc_xyz[1],
                            (int64_t) loc_xyz[2]);

            auto iter = voxel_map.find(position);
            if (iter != voxel_map.end()) {
                voxel_map[position]->temp_points_.push_back(p_v);
                voxel_map[position]->new_points_num_++;
            } else {
                auto *octo_tree = new UnionFindNode();
                voxel_map[position] = octo_tree;
                voxel_map[position]->voxel_center_[0] = (0.5 + static_cast<double>(position.x)) * voxel_size;
                voxel_map[position]->voxel_center_[1] = (0.5 + static_cast<double>(position.y)) * voxel_size;
                voxel_map[position]->voxel_center_[2] = (0.5 + static_cast<double>(position.z)) * voxel_size;
                voxel_map[position]->temp_points_.push_back(p_v);
                voxel_map[position]->new_points_num_++;
            }
        }
        
        for (auto & iter : voxel_map) {
            iter.second->InitUnionFindNode();
        }
        init_map = true;
    }

    void TransformLidar2World(const pcl::PointCloud<PointType>::Ptr cloudin, pcl::PointCloud<pcl::PointXYZI>::Ptr& cloudout){
        cloudout->clear();
        for(size_t i = 0; i < cloudin->size();i++){
            pcl::PointXYZI p_c = cloudin->points[i];
            Eigen::Vector3d p(p_c.x, p_c.y, p_c.z);
            p = R * p + t;
            pcl::PointXYZI pi;
            pi.x = static_cast<float>(p(0));
            pi.y = static_cast<float>(p(1));
            pi.z = static_cast<float>(p(2));
            pi.intensity = p_c.intensity;
            cloudout->points.push_back(pi);
        }
        
    }

